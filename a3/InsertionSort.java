package a3;

import java.util.Arrays;

/**
 *
 * @author Karina
 */
public class InsertionSort {
    public static void insertionSort(int[] arr) {
        int n = arr.length;
        for (int i = 1; i < n; i++) {
            int key = arr[i];
            int j = i - 1;
            while (j >= 0 && arr[j] > key) {
                arr[j + 1] = arr[j];
                j--;
            }
            arr[j + 1] = key;

            // Exibe o estado do array após cada iteração
            System.out.println("Passo " + i + ": " + Arrays.toString(arr));;
        }
    }
}