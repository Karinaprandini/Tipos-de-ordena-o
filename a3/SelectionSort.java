package a3;

import java.util.Arrays;

/**
 *
 * @author Karina
 */
public class SelectionSort {
    public static void selectionSort(int[] arr) {
        int n = arr.length;
        for (int i = 0; i < n - 1; i++) {
            int minIdx = i;
            for (int j = i + 1; j < n; j++) {
                if (arr[j] < arr[minIdx]) {
                    minIdx = j;
                }
            }
            int temp = arr[minIdx];
            arr[minIdx] = arr[i];
            arr[i] = temp;

            // Exibe o estado do array após cada iteração
            System.out.println("Passo " + (i + 1) + ": " + Arrays.toString(arr));
        }
    }
}

    
